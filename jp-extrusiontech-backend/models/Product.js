const mongoose = require('mongoose');

const ProductSchema = new mongoose.Schema({
  partNo: { 
    type: String, 
    required: true, 
    unique: true 
  },
  description: { 
    type: String, 
    required: true 
  },
  imagePath: { 
    type: String, 
    default: '/images/default.jpg' 
  },
  createdAt: { 
    type: Date, 
    default: Date.now 
  },
  updatedAt: { 
    type: Date, 
    default: Date.now 
  }
});

module.exports = mongoose.model('Product', ProductSchema); 